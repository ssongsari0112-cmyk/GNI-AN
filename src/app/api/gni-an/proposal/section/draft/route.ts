function formatContext(ctx: Record<string, string>): string {
  const lines = [
    ctx.pmcSourceText  && `[KOICA 집행계획(안) 원문 발췌 — PMC 모드: 아래 내용을 제안서 작성의 기본 바탕으로 반드시 활용하세요]\n${ctx.pmcSourceText}\n[/KOICA 집행계획(안)]`,
    ctx.title          && `사업명: ${ctx.title}`,
    ctx.country        && `대상 국가: ${ctx.country}`,
    ctx.region         && `대상 지역: ${ctx.region}`,
    ctx.field          && `사업 분야: ${ctx.field}`,
    ctx.organization   && `수행기관: ${ctx.organization}`,
    ctx.duration       && `사업 기간: ${ctx.duration}`,
    ctx.budget         && `총 예산: ${ctx.budget}`,
    ctx.idea           && `핵심 아이디어: ${ctx.idea}`,
    ctx.coreProblem    && `핵심 문제: ${ctx.coreProblem}`,
    ctx.targetBeneficiaries && `수혜 대상: ${ctx.targetBeneficiaries}`,
    ctx.interventionApproach && `개입 접근법: ${ctx.interventionApproach}`,
    ctx.expectedOutcomes     && `기대 성과: ${ctx.expectedOutcomes}`,
    ctx.problemTree    && `문제 분석 요약: ${ctx.problemTree}`,
    ctx.objectiveTree  && `목표 체계 요약: ${ctx.objectiveTree}`,
    ctx.pdm            && `사업 논리 모형(PDM) 전체 구조 — 아래 Outcome/Output/Activity를 빠짐없이 반영: ${ctx.pdm}`,
    ctx.expertInsights && `전문가 컨설팅 핵심 내용: ${ctx.expertInsights}`,
    ctx.projectSummary && `기획 요약: ${ctx.projectSummary}`,
  ].filter(Boolean);
  return lines.join('\n');
}

