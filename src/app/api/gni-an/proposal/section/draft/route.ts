export async function POST(req: NextRequest) {
  try {
    const { sectionId, projectContext } = await req.json();

    const promptFn = SECTION_PROMPTS[sectionId];
    if (!promptFn) {
      return NextResponse.json({ success: false, error: '지원하지 않는 섹션입니다.' }, { status: 400 });
    }

    if (!isOpenAIConfigured()) {
      const fallback = FALLBACK[sectionId];
      return NextResponse.json({ success: true, html: fallback || '<p>예시 내용을 직접 작성해 주세요.</p>' });
    }

    const ctx: Record<string, string> = {
      title:               projectContext?.title               || '',
      country:             projectContext?.country             || '',
      region:              projectContext?.region              || '',
      field:               projectContext?.field               || '',
      organization:        projectContext?.organization        || '굿네이버스',
      duration:            projectContext?.duration            || '',
      budget:              projectContext?.budget              || '',
      idea:                projectContext?.idea                || '',
      coreProblem:         projectContext?.coreProblem         || '',
      targetBeneficiaries: projectContext?.targetBeneficiaries || '',
      interventionApproach:projectContext?.interventionApproach|| '',
      expectedOutcomes:    projectContext?.expectedOutcomes    || '',
      problemTree:         projectContext?.problemTree         || '',
      objectiveTree:       projectContext?.objectiveTree       || '',
      expertInsights:      projectContext?.expertInsights      || '',
      projectSummary:      projectContext?.projectSummary      || '',
    };

    const html = await generateTextPro(
      [{ role: 'user', content: promptFn(ctx) }],
      SYSTEM_PROMPT
    );

    const cleaned = html
      .replace(/```html\n?/g, '')
      .replace(/```\n?/g, '')
      .trim();

    return NextResponse.json({ success: true, html: cleaned });
  } catch (error) {
    const message = error instanceof Error ? error.message : '초안 생성 중 오류가 발생했습니다.';
    return NextResponse.json({ success: false, error: message }, { status: 500 });
  }
}
