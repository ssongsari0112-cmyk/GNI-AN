export async function POST(req: NextRequest) {
  try {
    const { sectionId, projectContext } = await req.json();

    const promptFn = SECTION_PROMPTS[sectionId];
    if (!promptFn) {
      return NextResponse.json({ success: false, error: '지원하지 않는 섹션입니다.' }, { status: 400 });
    }

    if (!isOpenAIConfigured()) {
      return NextResponse.json({ success: true, html: FALLBACK_CONTENT[sectionId] || '' });
    }

    const systemPrompt = `당신은 KOICA 시민사회협력사업 제안서 전문 작성 AI입니다.
실제 제안서 수준의 전문적이고 구체적인 내용을 HTML 형식으로 작성하세요.
통계 수치와 출처를 포함하되, 프로젝트 정보에 맞게 자연스럽게 작성하세요.
반드시 허용된 HTML 태그(<p>, <strong>, <em>, <h3>, <ul>, <li>)만 사용하세요.`;

    const userMessage = promptFn(projectContext || {});

    const html = await generateText(
      [{ role: 'user', content: userMessage }],
      systemPrompt
    );

    const cleaned = html
      .replace(/```html\n?/g, '')
      .replace(/```\n?/g, '')
      .trim();

    return NextResponse.json({ success: true, html: cleaned });
  } catch (error) {
    const message = error instanceof Error ? error.message : '초안 생성 중 오류가 발생했습니다.';
    return NextResponse.json({ success: false, error: message }, { status: 500 });
  }
}
